# ###### ПОБУДОВА ДЕМО-СПИСКУ ПРАЦІВНИКІВ / ПОСТРОЕНИЕ ДЕМО-СПИСКА СОТРУДНИКОВ ######
def build_demo_employee_rows() -> list[tuple[str, str, str, str, str]]:
    """Повертає детермінований список працівників типового виробничого підприємства.
    Возвращает детерминированный список сотрудников типового производственного предприятия.
    """

    departments = (
        "Служба охорони праці",
        "Адміністрація заводу",
        "Механоскладальний цех",
        "Зварювальна дільниця",
        "Енергетична служба",
        "Ремонтна служба",
        "Склад і логістика",
        "Лабораторія контролю якості",
        "Транспортна служба",
    )
    positions = {
        "Служба охорони праці": ("Інспектор з охорони праці", "Провідний інженер з ОП"),
        "Адміністрація заводу": ("Начальник виробництва", "Майстер зміни"),
        "Механоскладальний цех": ("Слюсар-складальник", "Токар", "Фрезерувальник"),
        "Зварювальна дільниця": ("Електрогазозварник", "Контролер зварювальних робіт"),
        "Енергетична служба": ("Електромонтер", "Інженер-енергетик"),
        "Ремонтна служба": ("Слюсар-ремонтник", "Майстер ремонту"),
        "Склад і логістика": ("Комірник", "Водій навантажувача"),
        "Лабораторія контролю якості": ("Інженер ВТК", "Лаборант"),
        "Транспортна служба": ("Водій автотранспорту", "Механік автоколони"),
    }
    last_names = (
        "Іваненко", "Петренко", "Шевченко", "Коваль", "Бондар", "Ткаченко", "Мельник", "Кравченко", "Олійник",
        "Гриценко", "Демченко", "Лисенко", "Поліщук", "Савченко", "Романюк", "Білик", "Яценко", "Черненко",
    )
    first_names = (
        "Олександр", "Сергій", "Ігор", "Віктор", "Андрій", "Микола", "Юрій", "Роман", "Дмитро",
        "Олена", "Наталія", "Ірина", "Світлана", "Тетяна", "Людмила", "Марина",
    )
    patronymics = (
        "Олександрович", "Петрович", "Іванович", "Сергійович", "Миколайович", "Вікторович",
        "Олександрівна", "Петрівна", "Іванівна", "Сергіївна", "Миколаївна", "Вікторівна",
    )

    rows: list[tuple[str, str, str, str, str]] = []
    employee_count = 54
    for index in range(employee_count):
        department_name = departments[index % len(departments)]
        position_name = positions[department_name][index % len(positions[department_name])]
        full_name = f"{last_names[index % len(last_names)]} {first_names[index % len(first_names)]} {patronymics[index % len(patronymics)]}"
        personnel_number = f"{index + 1:04d}"
        employment_status = "archived" if index in {8, 17, 26, 35, 44, 53} else "active"
        rows.append((personnel_number, full_name, position_name, department_name, employment_status))

    rows[0] = ("0001", "Коваль Олена Вікторівна", "Інспектор з охорони праці", "Служба охорони праці", "active")
    rows[1] = ("0002", "Іваненко Сергій Петрович", "Начальник виробництва", "Адміністрація заводу", "active")
    return rows
